TSphereVertices::TSphereVertices()
{
    TSphereVertices::Init();
}

TSphereVertices::TSphereVertices( uint32_t radius )
    : m_radius(radius)
{
    TSphereVertices::Init();
}

uint32_t TSphereVertices::R() const
{
    return m_radius;
}

std::vector<uint32_t> const & TSphereVertices::Indices() const
{
    return m_indices;
}

void TSphereVertices::Init()
{

    glGenVertexArrays(1, &m_vao);
    m_vertices.clear();
    m_indices.clear();

    std::vector<glm::vec3> positions;
    std::vector<glm::vec2> uv;
    std::vector<glm::vec3> normals;

    for (uint32_t x = 0; x <= R(); ++x)
    {
        for (uint32_t y = 0; y <= R(); ++y)
        {
            float xSegment = (float)x / (float)R();
            float ySegment = (float)y / (float)R();
            float xPos = std::cos(xSegment * 2.0f * M_PI) * std::sin(ySegment * M_PI);
            float yPos = std::cos(ySegment * M_PI);
            float zPos = std::sin(xSegment * 2.0f * M_PI) * std::sin(ySegment * M_PI);

            positions.push_back(glm::vec3(xPos, yPos, zPos));
            uv.push_back(glm::vec2(xSegment, ySegment));
            normals.push_back(glm::vec3(xPos, yPos, zPos));
        }
    }

    bool oddRow = false;
    for (uint32_t y = 0; y < R(); ++y)
    {
        if (!oddRow) // even rows: y == 0, y == 2; and so on
        {
            for (uint32_t x = 0; x <= R(); ++x)
            {
                m_indices.push_back(y       * (R() + 1) + x);
                m_indices.push_back((y + 1) * (R() + 1) + x);
            }
        }
        else
        {
            for (int x = R(); x >= 0; --x)
            {
                m_indices.push_back((y + 1) * (R() + 1) + x);
                m_indices.push_back(y       * (R() + 1) + x);
            }
        }
        oddRow = !oddRow;
    }

    for (uint32_t i = 0; i < positions.size(); ++i)
    {
        m_vertices.push_back(positions[i].x);
        m_vertices.push_back(positions[i].y);
        m_vertices.push_back(positions[i].z);
        if (normals.size() > 0)
        {
            m_vertices.push_back(normals[i].x);
            m_vertices.push_back(normals[i].y);
            m_vertices.push_back(normals[i].z);
        }
        if (uv.size() > 0)
        {
            m_vertices.push_back(uv[i].x);
            m_vertices.push_back(uv[i].y);
        }
    }
    uint32_t ebo;
    glGenBuffers(1, &m_vbo);
    glGenBuffers(1, &ebo);
    glBindVertexArray(m_vao);
    glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
    glBufferData(GL_ARRAY_BUFFER, m_vertices.size() * sizeof(float), &m_vertices[0], GL_STATIC_DRAW);
    glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, ebo);
    glBufferData(GL_ELEMENT_ARRAY_BUFFER, m_indices.size() * sizeof(uint32_t), &m_indices[0], GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(2);
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
    glBindVertexArray(0);
}
